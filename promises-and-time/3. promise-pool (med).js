  for (let j = 0; j < n; j++) {
    pool.push(next());
  }

  // –§—É–Ω–∫—Ü–∏—è Promise.all –æ–∂–∏–¥–∞–µ—Ç, –ø–æ–∫–∞ –≤—Å–µ –ø—Ä–æ–º–∏—Å—ã –∏–∑ –º–∞—Å—Å–∏–≤–∞ `pool` –Ω–µ —Ä–∞–∑—Ä–µ—à–∞—Ç—Å—è.
  // –û–ø–µ—Ä–∞—Ç–æ—Ä await –ø–µ—Ä–µ–¥ Promise.all(pool) –Ω—É–∂–µ–Ω –¥–ª—è —Ç–æ–≥–æ, —á—Ç–æ–±—ã —Ñ—É–Ω–∫—Ü–∏—è promisePool –Ω–µ –∑–∞–≤–µ—Ä—à–∏–ª–∞—Å—å –¥–æ —Ç–µ—Ö –ø–æ—Ä,
  // –ø–æ–∫–∞ –≤—Å–µ –∑–∞–ø—É—â–µ–Ω–Ω—ã–µ –∞—Å–∏–Ω—Ö—Ä–æ–Ω–Ω—ã–µ ‚Äú–≤–æ—Ä–∫–µ—Ä—ã‚Äù (—Ü–µ–ø–æ—á–∫–∏ –≤—ã–∑–æ–≤–∞ next()) –Ω–µ –∑–∞–≤–µ—Ä—à–∞—Ç —Å–≤–æ—é —Ä–∞–±–æ—Ç—É.
  // –î–∞–≤–∞–π—Ç–µ –ø–æ–¥—Ä–æ–±–Ω–µ–µ —Ä–∞–∑–±–µ—Ä—ë–º, —á—Ç–æ –∑–¥–µ—Å—å –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç:
  //  1. –ê–≥—Ä–µ–≥–∞—Ü–∏—è –ø—Ä–æ–º–∏—Å–æ–≤: –ú–∞—Å—Å–∏–≤ pool —Å–æ–¥–µ—Ä–∂–∏—Ç –ø—Ä–æ–º–∏—Å—ã, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º—ã–µ –≤—ã–∑–æ–≤–∞–º–∏ —Ñ—É–Ω–∫—Ü–∏–∏ next() –∏–∑ –∫–∞–∂–¥–æ–≥–æ –∏–∑ –∑–∞–ø—É—â–µ–Ω–Ω—ã—Ö ¬´–≤–æ—Ä–∫–µ—Ä–æ–≤¬ª. 
  //     –ö–∞–∂–¥—ã–π —Ç–∞–∫–æ–π –ø—Ä–æ–º–∏—Å –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è–µ—Ç —Å–æ–±–æ–π —Ü–µ–ø–æ—á–∫—É –≤—ã–∑–æ–≤–æ–≤, –∫–æ—Ç–æ—Ä–∞—è –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –Ω–µ—Å–∫–æ–ª—å–∫–æ —Ñ—É–Ω–∫—Ü–∏–π –∏–∑ –∏—Å—Ö–æ–¥–Ω–æ–≥–æ –º–∞—Å—Å–∏–≤–∞.
  //  2. Promise.all: –§—É–Ω–∫—Ü–∏—è Promise.all(pool) —Å–æ–±–∏—Ä–∞–µ—Ç –≤—Å–µ —ç—Ç–∏ –ø—Ä–æ–º–∏—Å—ã –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –Ω–æ–≤—ã–π –ø—Ä–æ–º–∏—Å, –∫–æ—Ç–æ—Ä—ã–π —Ä–∞–∑—Ä–µ—à–∏—Ç—Å—è,
  //     –∫–æ–≥–¥–∞ –≤—Å–µ –ø—Ä–æ–º–∏—Å—ã –≤–Ω—É—Ç—Ä–∏ –º–∞—Å—Å–∏–≤–∞ –±—É–¥—É—Ç —Ä–∞–∑—Ä–µ—à–µ–Ω—ã. –≠—Ç–æ –ø–æ–∑–≤–æ–ª—è–µ—Ç –∞–≥—Ä–µ–≥–∏—Ä–æ–≤–∞—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç —Ä–∞–±–æ—Ç—ã –≤—Å–µ—Ö –≤–æ—Ä–∫–µ—Ä–æ–≤.
  //  3. –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ await: –ó–∞–ø–∏—Å—å await Promise.all(pool) –∑–∞—Å—Ç–∞–≤–ª—è–µ—Ç –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ promisePool –ø—Ä–∏–æ—Å—Ç–∞–Ω–æ–≤–∏—Ç—å—Å—è –Ω–∞ —ç—Ç–æ–º –º–µ—Å—Ç–µ
  //  –¥–æ —Ç–µ—Ö –ø–æ—Ä, –ø–æ–∫–∞ –∞–≥—Ä–µ–≥–∏—Ä—É—é—â–∏–π –ø—Ä–æ–º–∏—Å –Ω–µ –∑–∞–≤–µ—Ä—à–∏—Ç—Å—è. –≠—Ç–æ –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç, —á—Ç–æ —Ñ—É–Ω–∫—Ü–∏—è promisePool –≤–µ—Ä–Ω—ë—Ç —É–ø—Ä–∞–≤–ª–µ–Ω–∏–µ —Ç–æ–ª—å–∫–æ –ø–æ—Å–ª–µ —Ç–æ–≥–æ,
  //  –∫–∞–∫ –≤—Å–µ –∞—Å–∏–Ω—Ö—Ä–æ–Ω–Ω—ã–µ –æ–ø–µ—Ä–∞—Ü–∏–∏ (–≤—Å–µ —Ü–µ–ø–æ—á–∫–∏ –≤—ã–∑–æ–≤–∞ next()) –∑–∞–≤–µ—Ä—à–µ–Ω—ã. –ï—Å–ª–∏ –Ω–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å await, —Ç–æ —Ñ—É–Ω–∫—Ü–∏—è promisePool,
  //  –±—É–¥—É—á–∏ –∞—Å–∏–Ω—Ö—Ä–æ–Ω–Ω–æ–π, —Å—Ä–∞–∑—É –≤–µ—Ä–Ω—ë—Ç –ø—Ä–æ–º–∏—Å, –∫–æ—Ç–æ—Ä—ã–π –º–æ–∂–µ—Ç –±—ã—Ç—å –≤ —Å–æ—Å—Ç–æ—è–Ω–∏–∏ ¬´pending¬ª, –Ω–µ –¥–æ–∂–∏–¥–∞—è—Å—å –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –≤—Å–µ—Ö –≤–Ω—É—Ç—Ä–µ–Ω–Ω–∏—Ö –∑–∞–¥–∞—á.
  //  –≠—Ç–æ –Ω–∞—Ä—É—à–∏–ª–æ –±—ã —Ç—Ä–µ–±–æ–≤–∞–Ω–∏–µ –∑–∞–¥–∞—á–∏, —Å–æ–≥–ª–∞—Å–Ω–æ –∫–æ—Ç–æ—Ä–æ–º—É –ø—Ä–æ–º–∏—Å, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º—ã–π promisePool, –¥–æ–ª–∂–µ–Ω —Ä–∞–∑—Ä–µ—à–∞—Ç—å—Å—è –∏–º–µ–Ω–Ω–æ –ø–æ—Å–ª–µ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è
