  const pool = [];

  // –ó–∞–ø—É—Å–∫–∞–µ–º –æ–¥–Ω–æ–≤—Ä–µ–º–µ–Ω–Ω–æ `n` "–≤–æ—Ä–∫–µ—Ä–æ–≤". –¢–æ –µ—Å—Ç—å, –µ—Å–ª–∏ n = 2, —Ç–æ –æ–¥–Ω–æ–≤—Ä–µ–º–µ–Ω–Ω–æ
