/**
 * –ó–∞–¥–∞—á–∞: LeetCode 2757. Generate Circular Array Values
 * 
 * –î–∞–Ω –∫—Ä—É–≥–æ–≤–æ–π (—Ü–∏–∫–ª–∏—á–µ—Å–∫–∏–π) –º–∞—Å—Å–∏–≤ arr –∏ —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω—ã–π —Å—Ç–∞—Ä—Ç–æ–≤—ã–π –∏–Ω–¥–µ–∫—Å startIndex.
 * –ù—É–∂–Ω–æ –≤–µ—Ä–Ω—É—Ç—å –æ–±—ä–µ–∫—Ç-–≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä gen, –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–∏ –∫–∞–∂–¥–æ–º –≤—ã–∑–æ–≤–µ gen.next()
 * –≤—ã–¥–∞—ë—Ç —ç–ª–µ–º–µ–Ω—Ç—ã –º–∞—Å—Å–∏–≤–∞ arr –ø–æ ‚Äú–∫–æ–ª—å—Ü—É‚Äù.
 *
 * –ü–µ—Ä–≤–æ–µ –æ–±—Ä–∞—â–µ–Ω–∏–µ gen.next() –±–µ–∑ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ –¥–æ–ª–∂–Ω–æ –≤–µ—Ä–Ω—É—Ç—å arr[startIndex].
 * –ü—Ä–∏ –∫–∞–∂–¥–æ–º –ø–æ—Å–ª–µ–¥—É—é—â–µ–º –≤—ã–∑–æ–≤–µ gen.next(jump) –µ–º—É –ø–µ—Ä–µ–¥–∞—ë—Ç—Å—è —Ü–µ–ª–æ–µ jump.
 *
 * –ü—Ä–∞–≤–∏–ª–∞ —Å–º–µ—â–µ–Ω–∏—è:
 * - –µ—Å–ª–∏ jump > 0, —Å–¥–≤–∏–≥–∞–µ–º—Å—è –≤–ø–µ—Ä—ë–¥ –Ω–∞ jump —à–∞–≥–æ–≤; –¥–æ—Å—Ç–∏–≥–Ω—É–≤ –∫–æ–Ω—Ü–∞, –ø–µ—Ä–µ—Ö–æ–¥–∏–º –≤ –Ω–∞—á–∞–ª–æ;
 * - –µ—Å–ª–∏ jump < 0, —Å–¥–≤–∏–≥–∞–µ–º—Å—è –Ω–∞–∑–∞–¥ –Ω–∞ |jump| —à–∞–≥–æ–≤; –¥–æ—Å—Ç–∏–≥–Ω—É–≤ –Ω–∞—á–∞–ª–∞, –ø–µ—Ä–µ—Ö–æ–¥–∏–º –≤ –∫–æ–Ω–µ—Ü.
 *
 * –ü—Ä–∏–º–µ—Ä 1:
 *   const arr = [1,2,3,4,5], startIndex = 0;
 *   const gen = cycleGenerator(arr, startIndex);
 *   gen.next().value    // 1,  index = 0
 *   gen.next(1).value   // 2,  index = 1
 *   gen.next(2).value   // 4,  index = 3
 *   gen.next(6).value   // 5,  index = 4  (3 ‚Üí 4 ‚Üí 0 ‚Üí 1 ‚Üí 2 ‚Üí 3 ‚Üí 4)
 *
 * –ü—Ä–∏–º–µ—Ä 2:
 *   const arr = [10,11,12,13,14,15], startIndex = 1;
 *   const gen = cycleGenerator(arr, startIndex);
 *   gen.next().value    // 11, index = 1
 *   gen.next(1).value   // 12, index = 2
 *   gen.next(4).value   // 10, index = 0  (2 ‚Üí 3 ‚Üí 4 ‚Üí 5 ‚Üí 0)
 *   gen.next(0).value   // 10, index = 0
 *   gen.next(-1).value  // 15, index = 5
 *   gen.next(-3).value  // 12, index = 2  (5 ‚Üí 4 ‚Üí 3 ‚Üí 2)
 *
 * –ü—Ä–∏–º–µ—Ä 3:
 *   const arr = [2,4,6,7,8,10], startIndex = 3;
 *   const gen = cycleGenerator(arr, startIndex);
 *   gen.next().value    // 7,  index = 3
 *   gen.next(-4).value  // 10, index = 5 (3 ‚Üí 2 ‚Üí 1 ‚Üí 0 ‚Üí 5)
 *   gen.next(5).value   // 8,  index = 4 (5 ‚Üí 0 ‚Üí 1 ‚Üí 2 ‚Üí 3 ‚Üí 4)
 *   gen.next(-3).value  // 4,  index = 1 (4 ‚Üí 3 ‚Üí 2 ‚Üí 1)
 *   gen.next(10).value  // 10, index = 5 (1 ‚Üí ‚Ä¶ ‚Üí 5)
 *
 * –û–≥—Ä–∞–Ω–∏—á–µ–Ω–∏—è:
 *   1 ‚â§ arr.length ‚â§ 10‚Å¥
 *   1 ‚â§ steps.length ‚â§ 100
 *   ‚Äì10‚Å¥ ‚â§ arr[i], steps[i] ‚â§ 10‚Å¥
 *   0 ‚â§ startIndex < arr.length
 */



function* cycleGenerator(arr, startIndex) {
  const n = arr.length;

  /* 
   * –ó–∞–ø—É—Å–∫–∞–µ–º –±–µ—Å–∫–æ–Ω–µ—á–Ω—ã–π —Ü–∏–∫–ª,
   * —á—Ç–æ–±—ã –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä –º–æ–≥ –≤—ã–¥–∞–≤–∞—Ç—å –Ω–æ–≤—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è
   * –¥–æ —Ç–µ—Ö –ø–æ—Ä, –ø–æ–∫–∞ –µ–≥–æ —è–≤–Ω–æ –Ω–µ –æ—Å—Ç–∞–Ω–æ–≤—è—Ç.
   */
  while (true) {

    /*
     * –ü–µ—Ä–≤—ã–π –≤—ã–∑–æ–≤ generator.next() ‚Äì –≤—Å–µ–≥–¥–∞ –±–µ–∑ –∞—Ä–≥—É–º–µ–Ω—Ç–∞, 
     * –æ–Ω –Ω–∞—á–∏–Ω–∞–µ—Ç –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç –ø–µ—Ä–≤–æ–≥–æ yield.
     * –ù–∞ —ç—Ç–æ–π —Ç–æ—á–∫–µ –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä –ø—Ä–∏–æ—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ.
     * –ü—Ä–∏ –ø–µ—Ä–≤–æ–º –≤—ã–∑–æ–≤–µ gen.next():
     *  ‚Äì –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –¥–æ—Ö–æ–¥–∏—Ç –¥–æ yield arr[startIndex] –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ.
     *  ‚Äì –ø—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏–µ jump = ‚Ä¶ –µ—â—ë –Ω–µ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç, –∏ –∫–æ–¥ "–≤–∏—Å–Ω–µ—Ç" –ø—Ä—è–º–æ –∑–¥–µ—Å—å.
     *  ‚Äì —Å—Ç—Ä–æ–∫–∞ –Ω–∏–∂–µ, —Å –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ–º startIndex, –Ω–µ –≤—ã–ø–æ–ª–Ω–∏—Ç—Å—è.
     *
     * 1) –ü–∞—É–∑–∞ –∏ –æ—Ç–¥–∞—á–∞ –∑–Ω–∞—á–µ–Ω–∏—è:
     *    ‚Äì –î–≤–∏–∂–æ–∫ –≤—Å—Ç—Ä–µ—á–∞–µ—Ç `yield arr[startIndex]`,
     *      –≤—ã—á–∏—Å–ª—è–µ—Ç `arr[startIndex]` –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –µ–≥–æ
     *      –≤–Ω–µ—à–Ω–µ–º—É –∫–æ–¥—É –≤ `gen.next().value`.
     *    ‚Äì –ü—Ä–∏—Å–≤–æ–µ–Ω–∏–µ `jump = ‚Ä¶` –µ—â—ë –Ω–µ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç.
     *    ‚Äì –ì–µ–Ω–µ—Ä–∞—Ç–æ—Ä ¬´–∑–∞–º–æ—Ä–∞–∂–∏–≤–∞–µ—Ç—Å—è¬ª —Å—Ä–∞–∑—É –ø–æ—Å–ª–µ `yield`.
     *
     * 2) –í–æ–∑–æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∏ –ø—Ä–∏—ë–º –∞—Ä–≥—É–º–µ–Ω—Ç–∞:
     *    ‚Äì –ü—Ä–∏ –≤—ã–∑–æ–≤–µ `gen.next(x)` –ø–µ—Ä–µ–¥–∞–Ω–Ω–æ–µ `x` —Å—Ç–∞–Ω–æ–≤–∏—Ç—Å—è
     *      —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–º —Ç–æ–≥–æ —Å–∞–º–æ–≥–æ `yield`.
     *    ‚Äì –í—ã—Ä–∞–∂–µ–Ω–∏–µ `yield arr[startIndex]` ¬´–≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç¬ª `x`,
     *      –∏ –æ–Ω–æ –ø—Ä–∏—Å–≤–∞–∏–≤–∞–µ—Ç—Å—è –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π `jump`.
     *
     * –ü—Ä–∏–º–µ—Ä:
     *   const gen = cycleGenerator([10,20,30], 0);
     *
     *   // –ü–µ—Ä–≤—ã–π –≤—ã–∑–æ–≤:
     *   gen.next();      // => { value: 10, done: false }
     *   //   ‚Äì –æ—Ç–¥–∞—ë—Ç 10, jump = undefined
     *
     *   // –í—Ç–æ—Ä–æ–π –≤—ã–∑–æ–≤:
     *   gen.next(5);     // => { value: 30, done: false }
     *   //   ‚Äì –ø—Ä–∏—Å–≤–∞–∏–≤–∞–µ—Ç jump = 5, startIndex = (0+5)%3 = 2
     */
    const jump = yield arr[startIndex];

    /*
     * –ü–æ—Å–ª–µ –≤–æ–∑–æ–±–Ω–æ–≤–ª–µ–Ω–∏—è (–≤—Ç–æ—Ä–æ–π –∏ –ø–æ—Å–ª–µ–¥—É—é—â–∏–µ gen.next(value)):
     * —Å—é–¥–∞ –ø–µ—Ä–µ–¥–∞—ë—Ç—Å—è –∑–Ω–∞—á–µ–Ω–∏–µ value, –∏ —É–∂–µ –∑–∞—Ç–µ–º —ç—Ç–æ—Ç –±–ª–æ–∫ –æ—Ç—Ä–∞–±–æ—Ç–∞–µ—Ç.
     *
     * –ü–µ—Ä–µ—Å—á–∏—Ç—ã–≤–∞–µ–º startIndex —Å–ª–µ–¥—É—é—â–∏–º –æ–±—Ä–∞–∑–æ–º:
     *  a) startIndex + jump ‚Äî –¥–≤–∏–≥–∞–µ–º—Å—è –≤–ø–µ—Ä—ë–¥ (jump > 0) –∏–ª–∏ –Ω–∞–∑–∞–¥ (jump < 0).
     *       –ü—Ä–∏–º–µ—Ä 1: n = 5, startIndex = 3, jump = 2 ‚Üí 3 + 2 = 5
     *  b) (‚Ä¶ % n) ‚Äî –±–µ—Ä—ë–º –æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è, —á—Ç–æ–±—ã —É–π—Ç–∏ –æ—Ç ¬´–≤—ã—Ö–æ–¥–∞¬ª –∑–∞ –¥–ª–∏–Ω—É –º–∞—Å—Å–∏–≤–∞.
     *       –ü—Ä–∏–º–µ—Ä 1: 5 % 5 = 0
     *  c) + n ‚Äî —Å–¥–≤–∏–≥–∞–µ–º –¥–∏–∞–ø–∞–∑–æ–Ω –≤ –Ω–µ–æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω—É—é –∑–æ–Ω—É. –î–∞–∂–µ –µ—Å–ª–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç
     *    –ø—Ä–µ–¥—ã–¥—É—â–µ–π –æ–ø–µ—Ä–∞—Ü–∏–∏ –±—ã–ª –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω—ã–º, –º—ã –ø—Ä–æ—Å—Ç–æ –ø—Ä–∏–±–∞–≤–ª—è–µ–º –ª–∏—à–Ω–µ–µ n,
     *    —á—Ç–æ –≤—ã–≤–µ–¥–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ –∑–∞ –¥–∏–ø–∞–ø–∑–æ–Ω n-1. –ü–æ—Å–ª–µ–¥—É—é—â–∞—è –æ–ø–µ—Ä–∞—Ü–∏—è % n (d)
     *    –≤–µ—Ä–Ω–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ –≤ —Ä–∞–º–∫–∏ –¥–∏–∞–ø–æ–∑–æ–Ω–∞ –æ—Ç 0 –¥–æ n‚àí1.
     *       –ü—Ä–∏–º–µ—Ä 1: 0 + 5 = 5 
     *  d) –µ—â—ë –æ–¥–∏–Ω % n ‚Äî –æ–∫–æ–Ω—á–∞—Ç–µ–ª—å–Ω–æ –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ–º, —á—Ç–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç –±—É–¥–µ—Ç –≤ [0‚Ä¶n‚àí1],
     *       –æ–±–µ—Å–ø–µ—á–∏–≤–∞—è ¬´–∫–æ–ª—å—Ü–µ–≤–æ–π¬ª –ø–µ—Ä–µ—Ö–æ–¥.
     *       –ü—Ä–∏–º–µ—Ä 1: 5 % 5 = 0 ‚Üí –Ω–æ–≤—ã–π startIndex = 0
     *
     * –ü—Ä–∏–º–µ—Ä 2 (–æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω—ã–π —à–∞–≥): n = 5, startIndex = 1, jump = -3
     *  a) 1 + (‚àí3) = -2
     *  b) -2 % 5 = -2
     *  c) -2 + 5 = 3
     *  d) 3 % 5 = 3 ‚Üí –Ω–æ–≤—ã–π startIndex = 3
     */
    startIndex = (((startIndex + jump) % n) + n) % n;
  }
}






